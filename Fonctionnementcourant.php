<b> Fonctionnement courant:</b>
	
POUBELLES
- Mettre les déchets compostables dans le composteur du potager
- Le reste va dans les poubelles collectives, sous le pin au premier virage de la route d'Araules. 
- La déchetterie d'Yssingeaux est sur la route de St Etienne, à gauche après le rond point "Défi Mode". Au niveau de l'embranchement, il y a en face, donc à droite, un casse-bouteille.
 
BOIS
- Les bûches se trouvent dans la remise, avec de quoi les fendre, et le petit bois est sous la montée de grange. Pensez à renouveler le stock de petit bois. Votre participation aux frais servira notamment à renouveler le stock de bûches.

GAZ
- Les bouteilles sont situées à l’extérieur, sur le côté de la maison, au niveau de la fenêtre de la cuisine.
- Si une bouteille est vide, passer sur la bouteille de rechange avec le commutateur situé au-dessus des bouteilles.
- Remplacez la bouteille (propane), on en trouve à Intermarché, cela vous sera remboursé (prévenir l’association COUZ’TOUJOURS et lui transmettre la note).
- Si le gaz ne sort pas, activer la vanne ‘OUVERT/FERME’ située sous l’évier dans la cuisine. Il peut être nécessaire d’effectuer un va et vient de cette vanne pour réamorcer le gaz.

INTENDANCE, ACHATS, REPARATIONS
- En dehors des temps forts, chacun pourvoit à ses besoins courants en matière d'intendance. Mais comme pour le gaz, les dépenses collectives sont remboursées à celui qui s'en est chargé : petit matériel de bricolage, de jardinage, cuisine ou autre. Gardez vos factures, et voir ci-dessus "participation".
- Pour de grosses réparations, ou les gros achats, sauf urgence parlez-en d'abord au CA de l’Association (voir fiche contact)

